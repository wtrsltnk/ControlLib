#include "stdafx.h"

//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

CWPropertieDialog::CWPropertieDialog()
{
}

CWPropertieDialog::~CWPropertieDialog()
{
}

////////////////////////////////////////////////////////////////////////////////
/// Purpose : 
/// Input   : 
/// Output  : 
////////////////////////////////////////////////////////////////////////////////
LRESULT CWPropertieDialog::Init(DWORD dwResource)
{
	this->m_dwResourceDlg = dwResource;
	return TRUE;
}

////////////////////////////////////////////////////////////////////////////////
/// Purpose : 
/// Input   : 
/// Output  : 
////////////////////////////////////////////////////////////////////////////////
LRESULT CWPropertieDialog::Show(HWND hParent)
{
	this->m_hParent = hParent;

	return CWDialog::ShowModalless(this->m_hInstance, this->m_hParent);
}

////////////////////////////////////////////////////////////////////////////////
/// Purpose : 
/// Input   : 
/// Output  : 
////////////////////////////////////////////////////////////////////////////////
LRESULT CWPropertieDialog::Close()
{
	if (this->m_hDlg)
	{
		::EndDialog(this->m_hDlg, 0);
		this->m_hDlg = NULL;
		this->m_hParent = NULL;
	}
	return TRUE;
}


////////////////////////////////////////////////////////////////////////////////
/// Purpose : 
/// Input   : 
/// Output  : 
////////////////////////////////////////////////////////////////////////////////
void CWPropertieDialog::DlgProc(UINT uMsg, WPARAM wParam, LPARAM lParam)
{
	switch (uMsg)
	{
	case WM_INITDIALOG:
	case WM_COMMAND:
	case WM_LBUTTONDOWN:
	case WM_MOUSEMOVE:
	case WM_LBUTTONUP:
		{
			::SendMessage(this->m_hParent, uMsg, wParam, lParam);
			break;
		}
	}
	CWDialog::DlgProc(uMsg, wParam, lParam);
}