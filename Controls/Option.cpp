//////////////////////////////////////////////////////////////////////

#include "stdafx.h"

//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

CWOption::CWOption()
{
	this->m_dwStyle			= WS_CHILD | WS_VISIBLE | BS_NOTIFY | BS_AUTORADIOBUTTON;
	this->m_strClassName	= "BUTTON";
	this->m_strTitle		= "Titel";
}

CWOption::~CWOption()
{
}

////////////////////////////////////////////////////////////////////////////////
/// Purpose : 
/// Input   : 
/// Output  : 
////////////////////////////////////////////////////////////////////////////////
int CWOption::GetState()
{
	return SendMessage(m_hWnd, BM_GETCHECK, 0, 0);
}

////////////////////////////////////////////////////////////////////////////////
/// Purpose : 
/// Input   : 
/// Output  : 
////////////////////////////////////////////////////////////////////////////////
void CWOption::SetState(int state)
{
	SendMessage(m_hWnd, BM_SETCHECK, (WPARAM)state, 0);
}
