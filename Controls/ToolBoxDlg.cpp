//////////////////////////////////////////////////////////////////////

#include "stdafx.h"

//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

CWToolBoxDlg::CWToolBoxDlg()
{
}

CWToolBoxDlg::~CWToolBoxDlg()
{
}

////////////////////////////////////////////////////////////////////////////////
/// Purpose : 
/// Input   : 
/// Output  : 
////////////////////////////////////////////////////////////////////////////////
LRESULT CWToolBoxDlg::Init(DWORD dwResource)
{
	this->m_dwResourceDlg = dwResource;
	return TRUE;
}

////////////////////////////////////////////////////////////////////////////////
/// Purpose : 
/// Input   : 
/// Output  : 
////////////////////////////////////////////////////////////////////////////////
LRESULT CWToolBoxDlg::Show(HWND hParent)
{
	this->m_hParent = hParent;

	return CWDialog::ShowModalless(this->m_hInstance, this->m_hParent);
}

////////////////////////////////////////////////////////////////////////////////
/// Purpose : 
/// Input   : 
/// Output  : 
////////////////////////////////////////////////////////////////////////////////
LRESULT CWToolBoxDlg::Close()
{
	if (this->m_hDlg)
	{
		::EndDialog(this->m_hDlg, 0);
		this->m_hDlg = NULL;
		this->m_hParent = NULL;
	}
	return TRUE;
}

////////////////////////////////////////////////////////////////////////////////
/// Purpose : 
/// Input   : 
/// Output  : 
////////////////////////////////////////////////////////////////////////////////
void CWToolBoxDlg::DlgProc(UINT uMsg, WPARAM wParam, LPARAM lParam)
{
	switch (uMsg)
	{
	case WM_INITDIALOG:
	case WM_COMMAND:
	case WM_NOTIFY:
	case WM_LBUTTONDOWN:
	case WM_MOUSEMOVE:
	case WM_LBUTTONUP:
	case WM_SCROLLDLGS:
	case 0x020A:
		{
			::SendMessage(this->m_hParent, uMsg, wParam, lParam);
			break;
		}
	}
	CWDialog::DlgProc(uMsg, wParam, lParam);
}